#Kollar om talet är fyrsiffrigt, kollar även om det är fyrsiffrigt fast det är negativt
def ar_fyrsirigt(tal):
    tal_str = str(tal)
    if len(tal_str) == 4:
        return True
    elif tal_str.startswith("-") and len(tal_str) == 5:
        return True
    else:
        return False

