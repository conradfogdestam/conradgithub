for t in testtal:
    if ar_fyrsirigt(t):
        print(Fore.GREEN + f'{t} är fyrsiffrigt') # GRÖN print för om det är ett fyrsiffrigt tal
    else:
        print(Fore.RED + f'{t} är inte fyrsiffrigt') #RÖD print om de inte är fyrsiffrigt
