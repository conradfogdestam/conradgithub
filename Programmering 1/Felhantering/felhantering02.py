def ar_fyrsirigt(tal):
    tal_str = str(tal) # kollar om det är fyrsiffrigt
    if len(tal_str) == 4:
        return True
    elif tal_str.startswith('-') and len(tal_str) == 5: #elif för om det skulle vara ett fyrsiffrigt negativt tal (om den börjar med '-')
        return True
    else:
        return False

